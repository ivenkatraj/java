const a = 5, b = 10;

console.log(`The given values are a=${a} & b=${b}`);

//Greater than symbol
console.log("a greater than b", a > b);

//Less than symbol
console.log("a less than b", a < b);

//Greater than or equal to symbol
console.log("a greater than or equal to b", a >= b);

//Less than or equal to symbol
console.log("a less than or equal to b", a <= b);

//Is equal
console.log("a is equal to b", a == b);

//Not equal
console.log("a not equal b", a != b);

//Strict equality
console.log("Strict equality: a===b", a === b);

//Strict Inequality
console.log(a !== b);